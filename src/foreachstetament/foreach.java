package foreachstetament;

public class foreach {
    public static void main(String[] args) {
        // Ejemplo con matriz
        int[] numeros = {1, 2, 3, 4, 5};
        System.out.println("Iterando sobre una matriz:");
        for (int numero : numeros) {
            System.out.print(numero + " ");

        }
    }

}
